import 'package:flutter/material.dart';

//==============================================================================
// 注意項目の優先度を表すEnum（列挙型）
//
// Enumを使うことで、誤った値の設定を防ぎ、コードの安全性を向上
//==============================================================================

/// 注意項目の優先度
///
/// 各優先度には対応する色と背景色が設定されています。
/// constコンストラクタを使用してコンパイル時に値を確定し、パフォーマンスを向上。
enum AttentionPriority {
  /// 情報レベル（青色系）
  info(Colors.blue, Colors.blue),

  /// 警告レベル（オレンジ色系）
  warning(Colors.orange, Color(0xFFFFF3CD)),

  /// 危険レベル（赤色系）
  danger(Colors.red, Color(0xFFF8D7DA));

  //============================================================================
  // Enumのコンストラクタ
  // 各列挙値に色情報を関連付け
  //============================================================================
  const AttentionPriority(this.color, this.backgroundColor);

  /// アイコンや境界線の色
  final Color color;

  /// カードの背景色
  final Color backgroundColor;
}

//==============================================================================
// 注意が必要な項目を表示するカードウィジェット
//
// 使用例: 機械詳細画面で点検や修理が必要な項目を目立つように表示
// 表示内容: 警告アイコン、タイトル、説明文、優先度に応じた色分け
//==============================================================================

/// 注意が必要な項目を表示するカードウィジェット
///
/// ユーザーの注意を引くため、通常のカードより目立つデザインを採用。
/// 優先度に応じて色分けし、重要度を視覚的に伝えます。
class AttentionItemCard extends StatelessWidget {
  //============================================================================
  // コンストラクタで受け取るデータ
  //============================================================================

  /// 表示するアイコン（例: Icons.warning, Icons.error）
  final IconData icon;

  /// 注意項目のタイトル（例: "ブレーキ", "エンジン異常"）
  final String title;

  /// 注意項目の詳細説明（例: "ブレーキワイヤーの点検を行ってください。"）
  final String description;

  /// 注意項目の優先度（色分けに使用）
  final AttentionPriority priority;

  /// カードがタップされた時の処理（オプショナル）
  final VoidCallback? onTap;

  //============================================================================
  // コンストラクタ
  //============================================================================
  const AttentionItemCard({
    super.key,
    required this.icon, // 必須: アイコン
    required this.title, // 必須: タイトル
    required this.description, // 必須: 説明文
    required this.priority, // 必須: 優先度
    this.onTap, // オプショナル: タップ時の処理
  });

  //============================================================================
  // UIを構築するメソッド
  //============================================================================
  @override
  Widget build(BuildContext context) {
    //==========================================================================
    // Paddingウィジェット: カード間の余白を設定
    //==========================================================================
    return Padding(
      padding: const EdgeInsets.symmetric(
        horizontal: 16, // 左右に16pxの余白
        vertical: 4, // 上下に4pxの余白
      ),

      //========================================================================
      // Cardウィジェット: Material Designのカード
      // 注意項目なので、優先度に応じた背景色を適用
      //========================================================================
      child: Card(
        elevation: 1, // 軽い影
        shape: RoundedRectangleBorder(
          borderRadius: BorderRadius.circular(12), // 角丸
        ),
        color: priority.backgroundColor, // 優先度に応じた背景色
        //======================================================================
        // InkWellウィジェット: タップ可能にし、リップルエフェクトを追加
        //======================================================================
        child: InkWell(
          onTap: onTap, // タップ時の処理
          borderRadius: BorderRadius.circular(12), // リップルエフェクトの角丸
          //====================================================================
          // Paddingウィジェット: カード内部の余白
          //====================================================================
          child: Padding(
            padding: const EdgeInsets.all(16), // 全方向に16pxの余白
            //==================================================================
            // Rowウィジェット: アイコンと情報を横並びで配置
            //==================================================================
            child: Row(
              children: [
                //==============================================================
                // 左側: 警告アイコン部分
                //==============================================================
                Container(
                  width: 40, // アイコンコンテナの幅
                  height: 40, // アイコンコンテナの高さ
                  decoration: BoxDecoration(
                    color: priority.color, // 優先度に応じたアイコン背景色
                    borderRadius: BorderRadius.circular(20), // 円形
                  ),
                  child: Icon(
                    icon, // 表示するアイコン
                    color: Colors.white, // 白いアイコン
                    size: 20, // アイコンのサイズ
                  ),
                ),

                //==============================================================
                // 中央: 情報表示部分
                //==============================================================
                const SizedBox(width: 16), // アイコンと情報の間に16pxの空白

                Expanded(
                  // 残りのスペースを使用
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.start, // 左寄せ
                    children: [
                      //========================================================
                      // タイトル
                      //========================================================
                      Text(
                        title,
                        style: TextStyle(
                          fontSize: 16,
                          fontWeight: FontWeight.bold,
                          color: priority.color, // 優先度に応じた文字色
                        ),
                      ),

                      //========================================================
                      // 説明文
                      //========================================================
                      const SizedBox(height: 4), // タイトルと説明文の間に4pxの空白
                      Text(
                        description,
                        style: TextStyle(
                          fontSize: 14,
                          color: priority.color.withOpacity(0.8), // 少し薄い色
                        ),
                      ),
                    ],
                  ),
                ),

                //==============================================================
                // 右側: 矢印アイコン（詳細画面への誘導）
                //==============================================================
                Icon(
                  Icons.chevron_right, // 右向き矢印
                  color: priority.color, // 優先度に応じた色
                ),
              ],
            ),
          ),
        ),
      ),
    );
  }
}
